package spring.pickMe.domain;

public class UrlVO {
	
	private int urlNo;
	private int postNo;
	private String yrl;
	public int getUrlNo() {
		return urlNo;
	}
	public void setUrlNo(int urlNo) {
		this.urlNo = urlNo;
	}
	public int getPostNo() {
		return postNo;
	}
	public void setPostNo(int postNo) {
		this.postNo = postNo;
	}
	public String getYrl() {
		return yrl;
	}
	public void setYrl(String yrl) {
		this.yrl = yrl;
	}
	
	
}
